//Comparision operators
console.log("Comparision operators");
let x = 4, y = '3';
console.log("x==y: ", x==y);   //Loose equality
console.log("x===y: ", x===y); //strict equality
console.log("x!=y ", x!=y);
console.log("x!==y ", x!==y);
console.log("x>y ", x>y);
console.log("x<y ", x<y);
console.log("x>=y ", x>=y);
console.log("x<=y ", x<=y);